/**
 * Resolves a high-quality preview image for a product.
 * Matches keywords against the product name, falling back to a category image.
 */
export function getProductImage(productName: string, categoryKey: string): string {
  // Try to find a precise keyword match
  const match = KEYWORD_IMAGES.find((item) => item.pattern.test(productName));
  if (match) {
    return match.url;
  }

  // Fallback to the category default
  return (
    CATEGORY_FALLBACKS[categoryKey] ??
    "https://images.unsplash.com/photo-1496181130204-755241524eab?auto=format&fit=crop&w=500&q=80"
  );
}
